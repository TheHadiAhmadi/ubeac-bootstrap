<p>
  CSS variables Added in v5.2.0 With v5.1.1, we standardized our required
  @imports across all our CSS bundles (including bootstrap.css,
  bootstrap-reboot.css, and bootstrap-grid.css) to include _root.scss. This adds
  :root level CSS variables to all bundles, regardless of how many of them are
  used in that bundle. Ultimately Bootstrap 5 will continue to see more CSS
  variables added over time, in order to provide more real-time customization
  without the need to always recompile Sass. Our approach is to take our source
  Sass variables and transform them into CSS variables. That way, even if you
  donâ€™t use CSS variables, you still have all the power of Sass. This is still
  in-progress and will take time to fully implement.
</p>
